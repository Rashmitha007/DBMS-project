    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int check=0;
        fid=jTextField1.getText();
        if(fid.equals(""))
        {
            check=1;
            JOptionPane.showMessageDialog(null, "Factory ID is required");
        }
        else
        {
           ResultSet rs=Select.getData("select * from signup where fact_id='"+fid+"'");
            try
            {
               if(rs.next())
               {
                   check=1;
                   jTextField2.setEditable(false);
                   jTextField1.setEditable(false);
                   jTextField2.setText(rs.getString(5));
               }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        if(check==0)
            JOptionPane.showMessageDialog(null, "Incorrect Factory ID");
    }//GEN-LAST:event_jButton1ActionPerformed

