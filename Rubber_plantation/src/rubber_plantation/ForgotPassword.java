    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int check=0;
        String SecurityQuestion=jTextField2.getText();
        String answer=jTextField3.getText();
        String newPassword=jPasswordField1.getText();
        if(answer.equals("") || newPassword.equals(""))
        {
            check=1;
            JOptionPane.showMessageDialog(null, "All Field is Required");
        }
        else
        {
            ResultSet rs=Select.getData("select * from signup where fact_id='"+fid+"'and secure='"+SecurityQuestion+"'and answer='"+answer+"'");
            try
            {
               if(rs.next())
               {
                   check=1;
                   InsertUpdateDelete.setData("update signup set password1='"+newPassword+"' where fact_id='"+fid+"'","Password Set Successfully");
                   //setVisible(false);
                   //new ForgotPassword().setVisible(true);
               }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
        if(check==0)
            JOptionPane.showMessageDialog(null,"Incorrect Answer");
    }//GEN-LAST:event_jButton2ActionPerformed

