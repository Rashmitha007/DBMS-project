    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sup_ID=ids.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            java.sql.ResultSet rs=st.executeQuery("select * from Unit where sup_id='"+sup_ID+"' and fact_id='"+i+"'");
            if(rs.next())
            {
                sid.setText(rs.getString(1));
                sname.setText(rs.getString(2));
                un.setText(rs.getString(3));
                ull.setText(rs.getString(4));
                f.setText(rs.getString(5));

                ids.setEditable(false);
                f.setEditable(false);
            }
            else
            JOptionPane.showMessageDialog(null,"Company ID does not Exists");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

