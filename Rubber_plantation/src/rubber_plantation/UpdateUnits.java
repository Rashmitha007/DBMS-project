    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String ssid=ids.getText();
        //String id=sid.getText();
        String name=sname.getText();
        String uname=un.getText();
        String unitl=ull.getText();
        String factid=f.getText();
        try
        {
            if(factid.equals(i)){
             Connection con=ConnectionProvider.getCon();
             Statement st=con.createStatement();
             st.executeUpdate("Update Unit set Sup_id='"+ssid+"',Sup_name='"+name+"',unit_name='"+uname+"',unit_loc='"+unitl+"',fact_id='"+factid+"' where Sup_id='"+ssid+"' and fact_id='"+factid+"'");
             JOptionPane.showMessageDialog(null, "Successfully Updated");
             setVisible(false);
             //new UpdateUnits(i).setVisible(true);
             
             new UnitsDetails(i).setVisible(true);
            }
                        else
            {
                JOptionPane.showMessageDialog(null,"Enter valid fact_id");
            }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

